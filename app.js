const state = {
  lang: getInitialLanguage(),
  filter: "all",
  cart: new Map(),
};

const productGrid = document.querySelector("#product-grid");
const cartLines = document.querySelector("#cart-lines");
const cartEmpty = document.querySelector("#cart-empty");
const cartTotal = document.querySelector("#cart-total");
const orderForm = document.querySelector("#order-form");
const orderStatus = document.querySelector("#order-status");

function getInitialLanguage() {
  const saved = localStorage.getItem("baanobauan-language");
  if (saved && translations[saved]) return saved;
  const browserLang = navigator.language.toLowerCase();
  if (browserLang.startsWith("th")) return "th";
  if (browserLang.startsWith("zh")) return "zh";
  return "en";
}

function t(path) {
  return path.split(".").reduce((value, key) => value?.[key], translations[state.lang]) ?? path;
}

function money(value) {
  return `฿${value.toLocaleString("th-TH")}`;
}

function setLanguage(lang) {
  state.lang = lang;
  localStorage.setItem("baanobauan-language", lang);
  document.documentElement.lang = lang === "zh" ? "zh-Hant" : lang;
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = t(node.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    node.placeholder = t(node.dataset.i18nPlaceholder);
  });
  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === lang);
  });
  renderProducts();
  renderCart();
}

function renderProducts() {
  const visibleProducts = products.filter((product) => state.filter === "all" || product.category === state.filter);
  productGrid.innerHTML = visibleProducts
    .map(
      (product) => `
        <article class="product-card${product.featured ? " featured-product-card" : ""}">
          <div class="product-media">
            <img src="${product.image}" alt="${product.name[state.lang]}">
            <span class="product-badge">${product.badge[state.lang]}</span>
          </div>
          <div class="product-body">
            <h3>${product.name[state.lang]}</h3>
            <p>${product.desc[state.lang]}</p>
          </div>
          <div class="product-footer">
            <span class="price">${t("product.from")} ${money(product.price)}</span>
            <button class="add-button" type="button" data-add="${product.id}">${t("product.add")}</button>
          </div>
        </article>
      `
    )
    .join("");
}

function renderCart() {
  const entries = [...state.cart.entries()];
  const hasItems = entries.length > 0;
  cartEmpty.hidden = hasItems;
  cartLines.innerHTML = entries
    .map(([id, qty]) => {
      const product = products.find((item) => item.id === id);
      return `
        <div class="cart-line">
          <span>
            <span class="cart-name">${product.name[state.lang]}</span>
            <span class="cart-meta">${money(product.price)} x ${qty}</span>
          </span>
          <span class="qty-control" aria-label="${t("product.qty")}">
            <button type="button" data-dec="${id}" aria-label="-">-</button>
            <span>${qty}</span>
            <button type="button" data-inc="${id}" aria-label="+">+</button>
          </span>
        </div>
      `;
    })
    .join("");
  const total = entries.reduce((sum, [id, qty]) => {
    const product = products.find((item) => item.id === id);
    return sum + product.price * qty;
  }, 0);
  cartTotal.textContent = money(total);
}

function addToCart(id, amount = 1) {
  const next = (state.cart.get(id) ?? 0) + amount;
  if (next <= 0) {
    state.cart.delete(id);
  } else {
    state.cart.set(id, next);
  }
  renderCart();
}

function buildOrderMessage(formData) {
  const labels = translations[state.lang].orderMessage;
  const items = [...state.cart.entries()].map(([id, qty]) => {
    const product = products.find((item) => item.id === id);
    return `- ${product.name[state.lang]} x ${qty} = ${money(product.price * qty)}`;
  });
  const total = [...state.cart.entries()].reduce((sum, [id, qty]) => {
    const product = products.find((item) => item.id === id);
    return sum + product.price * qty;
  }, 0);
  const methodLabel = formData.get("method") === "delivery" ? labels.delivery : labels.pickup;
  return [
    labels.title,
    "",
    items.join("\n"),
    "",
    `${labels.total}: ${money(total)}`,
    `${labels.customer}: ${formData.get("name") || "-"}`,
    `${labels.contact}: ${formData.get("phone") || "-"}`,
    `${labels.method}: ${methodLabel}`,
    `${labels.time}: ${formData.get("date") || "-"}`,
    `${labels.notes}: ${formData.get("notes") || "-"}`,
  ].join("\n");
}

document.addEventListener("click", (event) => {
  const addButton = event.target.closest("[data-add]");
  if (addButton) {
    addToCart(addButton.dataset.add);
    orderStatus.textContent = "";
  }

  const incButton = event.target.closest("[data-inc]");
  if (incButton) addToCart(incButton.dataset.inc);

  const decButton = event.target.closest("[data-dec]");
  if (decButton) addToCart(decButton.dataset.dec, -1);
});

